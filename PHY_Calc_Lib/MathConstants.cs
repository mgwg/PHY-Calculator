namespace PHY_Calc_Lib
{
  /// <summary>
  /// Holds constants used in various equations in physics. 
  /// </summary>
  public class MathConstants
  {
    /// <summary>
    ///  The ratio of a circle's circumference to its diameter. Approximated to 3.14159.
    /// </summary>
    public static readonly string PI = "3.14159";

    /// <summary>
    /// The number e, also known as Euler's number. Equal to 2.71828
    /// </summary>
    public static readonly string e = "2.71828";

    /// <summary>
    /// The Earth Gravitational constant. Equal to 9.81 m/s
    /// </summary>
    public static readonly string G = "9.81";

    /// <summary>
    /// The Speed of Light. Equal to 2.9979 * 10^8
    /// </summary>
    public static readonly string C = "2.9979E8";

    /// <summary>
    /// The Ideal Gas constant; units of energy per temperature increment per mole i.e pressure volume product. 
    /// Equal to 8.314
    /// </summary>
    public static readonly string R = "8.314";

    /// <summary>
    /// Avogadro's constant: the proportionality factor that relates the number of constituent particles (usually molecules, atoms or ions)
    /// in a sample with the amount of substance in that sample. Equal to 6.022 * 10^24
    /// </summary>
    public static readonly string N = "6.022E24";

    /// <summary>
    /// Coulomb constant (electrostatic constant): proportionality constant in electrostatics equations.
    /// Equal to 1.3806 * 10^-23
    /// </summary>
    public static readonly string K = "1.3806E-23";

    /// <summary>
    /// Electron-volt : the measure of Kinetic Energy gained by a single electron 
    /// accelerating from rest through an electric potential difference of one volt in vacuum. 
    /// Equal to: 1.6022 * 10^-19
    /// </summary>
    public static readonly string eV = "1.6022E-19";

    /// <summary>
    /// Elementary positive charge:
    /// electric charge carried by a single proton or, equivalently, the magnitude of the negative electric charge carried by a single electron,
    /// which has charge −1 e.
    /// Equal to 1.6022 * 10^-19
    /// </summary>
    public static readonly string q_e = "1.6022E-19";

    /// <summary>
    /// The mass of a stationary electron measured in kilograms . 
    /// Equal to 9.1094 * 10^-31
    /// </summary>
    public static readonly string m_e = "9.1094E-31";

    /// <summary>
    /// The mass of a stationary proton measured in kilograms.
    /// Equal to 1.6726 * 10^-27
    /// </summary>
    public static readonly string m_p = "1.6726E-27";

    /// <summary>
    /// Plank's constant : product of energy multiplied by time, a quantity called action. 
    /// Equal to 6.6261 * 10^-34
    /// </summary>
    public static readonly string h = "6.6261E-34";

    /// <summary>
    /// Reduced Plank's constant (H bar) : Plank's Constant divided by 2 PI. 
    /// Used to quantify angular momentum. Equal to 1.0546 * 10^-34
    /// </summary>
    public static readonly string ħ = "1.0546E-34";
  }
}
